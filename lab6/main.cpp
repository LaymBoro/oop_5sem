#include <iostream>
#include <stdexcept>


class LogicError : std::logic_error 
{
public:
    LogicError(const std::string& a) :std::logic_error(a) {}
    const char* what() 
    {
        return std::logic_error::what();
    }
};

class Tv
{
public:
    friend class Remote;
    enum { Off, On };
    enum { MinVal, MaxVal = 20 };
    enum { TV, DVD };

private:
    int state;
    int volume;
    int channel;
    int mode;
    int maxChannel;

public:
    Tv(int s = Off, int mc = 125) : state(s), volume(5), channel(1), mode(TV), maxChannel(mc) {};

    void onoff() { state ^= On; }

    bool ison() const { return state == On; }

    bool volup()
    {
        if (volume < MaxVal)
        {
            volume++;
            return true;
        }
        return false;
    }

    bool voldown() 
    {
        if (volume > MinVal)
        {
            volume--;
            return true;
        }
        return false;
    }

    void channelup()
    {
        if (channel < maxChannel)
        {
            channel++;
        }
        else
        {
            channel = maxChannel;
        }
    }

    void channeldown()
    {
        if (channel < maxChannel)
        {
            channel++;
        }
        else
        {
            channel = maxChannel;
        }
    }

    void set_mode() { mode ^= DVD; }

    void settings() const 
    {
        std::cout << "TV is " << (state == Off ? "Off" : "On") << "\n";
        if (state == On)
        {
            std::cout << "Channel: " << channel << "\n";
            std::cout << "Volume: " << volume << "\n";
            std::cout << "mode: " << (mode == TV ? "TV" : "DVD") << "\n";
        }
    }
};

class Remote 
{
public:
    enum Mode
    { 
        Normal,
        Interactive 
    };

private:
    int mode;

public:
    friend class Tv;
    Remote(int m = Tv::TV) : mode(Normal) {}

    bool volup(Tv& t)
    {
        if (t.volume < t.MaxVal) 
        {
            t.volume++;
            return true;
        }
        return false;
    }

    bool voldown(Tv& t) 
    {
        if (t.volume > t.MinVal) 
        {
            t.volume--;
            return true;
        }
        return false;
    }

    void onnof(Tv& t) { t.onoff(); }

    void channelup(Tv& t)
    {
        if (t.channel < t.maxChannel)
        {
            t.channel++;
        }
        else
        {
            t.channel = t.maxChannel;
        }
    }
  
    void chandown(Tv& t)
    {
        if (t.channel > 1)
        {
            t.volume--;
        }
        else
        {
            t.channel = 1;
        }
    }

    void set_chanel(Tv& t, int c) { t.channel = c; }

    void set_mode(Tv& t) { t.set_mode(); }

    void showMode() 
    {
        std::cout << "Mode: ";
        (mode == Normal) ? std::cout << "normal\n" : std::cout << "interactive\n";
    }
};

int main()
{
    Tv SamsungTV;
    std::cout << "Initial setting:" << "\n";
    SamsungTV.settings();
    SamsungTV.onoff();
    SamsungTV.channelup();
    SamsungTV.volup();
    SamsungTV.volup();
    std::cout << "\nNew setting:" << "\n";
    SamsungTV.settings();

    Remote greyR;
    greyR.set_chanel(SamsungTV, 45);
    greyR.volup(SamsungTV);
    greyR.volup(SamsungTV);
    std::cout << "\nNew remote setting:" << "\n";
    SamsungTV.settings();

    return 0;
}